'''
Python Any Function (Basic 1)

Example: Any function examples 
'''
iterable1 = [True, False, False]
iterable2 = [False, False, False]


result1 = any(iterable1)
result2 = any(iterable2)


# Display
print("Result 1:", result1)
print("Result 2:", result2)


'''
Output:
Result 1: True
Result 2: False
'''

