'''
Python Dictionary (Basic 1)
    - It's an unordered collection of items
    - Mutable Datatype
    - Key/Value pair data structure
'''

# Create dictionary of student

student = {
    'name': 'Alex Hales',
    'age': 25,
    'dob': '10/04/1996'
}

# Access attributes of student
print('Name:', student['name'])
print('Age:', student['age'])
print('DOB:', student['dob'])


'''
Output:

Name: Alex Hales
Age: 25
DOB: 10/04/1996
'''

