'''
Python Dictionary (Basic 2)
    - It's an unordered collection of items
    - Mutable Datatype
    - Key/Value pair data structure
'''

# Create dictionary of student
student = {
    'name': 'Alex Hales',
    'age': 25,
    'dob': '10/04/1996'
}

# Add element in dictionary
student['address'] = 'London'

# Update element in dictionary
student['dob'] = '11/04/1996'

# Display dictionary
print(student)

'''
Output:
{
    'name': 'Alex Hales', 
    'age': 25, 
    'dob': '11/04/1996', 
    'address': 'London'
}
'''
