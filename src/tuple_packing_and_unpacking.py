'''
Python Tuple Packing & Unpacking

Example: Packing a tuple, unpacking into 
         variables and print variables
'''

# Packing tuple
stats = ("Bradley Beal", 33, "Wizards")


# Unpacking tuple values
name, points, team = stats


# Print variables
print("Name:", name)
print("Points:", points)
print("Team:", team)


'''
Output:
Name: Bradley Beal
Points: 33
Team: Wizards
'''
